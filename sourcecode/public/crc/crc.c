unsigned short CRC16(unsigned short uchCRC, unsigned char *puchMsg, int usDataLen)
{
    unsigned char uIndex ;   /* will index into CRC lookup table */
    while (usDataLen--) /* pass through message buffer */
    {
        uIndex = (uchCRC >> 8) ^ *puchMsg++ ; /* calculate the CRC */
        uchCRC = (uchCRC << 8) ^ crc_tab16[uIndex];
    }
    return uchCRC;
}

