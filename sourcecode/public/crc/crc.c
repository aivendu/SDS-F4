unsigned long update_crc_32(unsigned long crcdata, char cchar)
{

    unsigned long tmp, long_c;

    long_c = 0x000000ffL & (unsigned long)cchar;

    tmp = crcdata ^ long_c;
    // crc = (crc >> 8) ^ crc_tab32[ tmp & 0xff ];


    crcdata = (crcdata >> 8) ^ crc32_tab[tmp & 0xff];

    return crcdata;

}  /* update_crc_32 */


uint32_t crc_32(char *buf, int16_t length)
{
    uint32_t crcdata;
    //char  buf[] = "123456789";
    uint16_t i;
    crcdata = 0xffffffffL;


    for (i = 0; i < length; i++)
    {
        crcdata = update_crc_32(crcdata, buf[i]);
    }

    crcdata ^= 0xffffffffL;


    return crcdata;
}
